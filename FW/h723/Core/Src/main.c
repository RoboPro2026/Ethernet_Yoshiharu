/**
 * @brief  Function implementing the defaultTask thread.
 * @param  argument: Not used
 * @retval None
 */
/* USER CODE END Header_StartDefaultTask */
void StartDefaultTask(void const *argument)
{
  /* init code for LWIP */
  MX_LWIP_Init();

  /* init code for USB_DEVICE */
  MX_USB_DEVICE_Init();
  /* USER CODE BEGIN 5 */
  const char *message = "Hello UDP message!\n\r";

  osDelay(1000);

  ip_addr_t PC_IPADDR;
  IP_ADDR4(&PC_IPADDR, 192, 168, 1, 1);

  struct udp_pcb *my_udp = udp_new();
  udp_connect(my_udp, &PC_IPADDR, 55151);
  struct pbuf *udp_buffer = NULL;

  // ネットワークインターフェースの取得
  extern struct netif gnetif;

  // リンクが確立するまで待機
  CDC_Transmit_HS((uint8_t *)"Waiting for link up...\r\n", strlen("Waiting for link up...\r\n"));
  osDelay(100); // USB CDC送信待ち

  while (!netif_is_link_up(&gnetif))
  {
    osDelay(100);
  }
  CDC_Transmit_HS((uint8_t *)"Link is UP!\r\n", strlen("Link is UP!\r\n"));
  osDelay(100); // USB CDC送信待ち

  // インターフェースがupするまで待機
  while (!netif_is_up(&gnetif))
  {
    osDelay(100);
  }
  CDC_Transmit_HS((uint8_t *)"Interface is UP!\r\n", strlen("Interface is UP!\r\n"));
  osDelay(100); // USB CDC送信待ち

  // さらに少し待機（ARPテーブル構築のため）
  osDelay(2000); // 2秒待機（Rx割り込みが動作するか確認）

  CDC_Transmit_HS((uint8_t *)"Starting UDP...\r\n", strlen("Starting UDP...\r\n"));
  osDelay(100);

  /* Infinite loop */
  for (;;)
  {
    // LED点滅など
    osDelay(1000);
    /* !! PBUF_RAM は正しい動作のために重要です !! */
    udp_buffer = pbuf_alloc(PBUF_TRANSPORT, strlen(message), PBUF_RAM);

    if (udp_buffer != NULL)
    {
      memcpy(udp_buffer->payload, message, strlen(message));
      err_t err = udp_send(my_udp, udp_buffer);
      pbuf_free(udp_buffer);

      // udp_sendの結果を表示
      if (err == ERR_OK)
      {
        CDC_Transmit_HS((uint8_t *)"UDP sent OK\r\n", strlen("UDP sent OK\r\n"));
      }
      else
      {
        char err_msg[50];
        snprintf(err_msg, sizeof(err_msg), "UDP send ERROR: %d\r\n", err);
        CDC_Transmit_HS((uint8_t *)err_msg, strlen(err_msg));
      }
    }
    else
    {
      CDC_Transmit_HS((uint8_t *)"udp_buffer is NULL\r\n", strlen("udp_buffer is NULL\r\n"));
    }

    HAL_GPIO_TogglePin(LED_1_GPIO_Port, LED_1_Pin);
    osDelay(1000);
  }
  /* USER CODE END 5 */
}

